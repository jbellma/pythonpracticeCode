# Using th range function. Python stops counting at the second value provided.
for value in range(1, 5):
    print(value)

# Using the range function to include the second value provided (5).
for value in range(1, 6):
    print(value)

# Using the list() function to convert the results of range() into a list.
numbers = list(range(1, 6))
print(numbers)
